// Set - უნიკალური მნიშვნელობები (განმეორება არ არის)
let სეტი = new Set([1, 2, 2, 3, 3, 3, 4]);
console.log(სეტი); // Set {1, 2, 3, 4}
სეტი.add(5);
სეტი.delete(1);
console.log(სეტი.has(2)); // true
console.log(სეტი.size);   // 4

// Map - გასაღები:მნიშვნელობა (ნებისმიერი ტიპის გასაღები)
let მეპი = new Map();
მეპი.set("სახელი", "დავითი");
მეპი.set(1, "პირველი");
მეპი.set(true, "ჭეშმარიტი");

console.log(მეპი.get("სახელი")); // დავითი
console.log(მეპი.size);           // 3

// Map-ის გასავლელად
მეპი.forEach((მნიშვნ, გასაღ) => {
    console.log(`${გასაღ}: ${მნიშვნ}`);
});


