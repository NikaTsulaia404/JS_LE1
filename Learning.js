// Promise - დაპირება რომ მომავალში მოვა შედეგი
function მოიტანეი(მოიტანი) {
    return new Promise((resolve, reject) => {
        setTimeout(() => {
            if (მოიტანი) {
                resolve("მონაცემები მოვიდა!");
            } else {
                reject("შეცდომა!");
            }
        }, 1000);
    });
}

// .then() .catch() სტილი
მოიტანეი(true)
    .then(შედეგი => console.log(შედეგი))
    .catch(შეცდომა => console.log(შეცდომა));

// async/await სტილი (უფრო წაკითხვადი)
async function მიღება() {
    try {
        let შედ = await მოიტანეი(true);
        console.log(შედ);
    } catch (err) {
        console.log("შეცდომა:", err);
    }
}
მიღება();


